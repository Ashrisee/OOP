package myproject;

public class auto_cast {
	public static void main(String args[]) {
		int a = 2;
		double b=3.4;
		b=a;//auto
		System.out.println("b="+b);
		System.out.println("a="+a);
		double d=5.8;
		a=(int)d;//casting.manual
		System.out.println("a="+a);
	}

}
